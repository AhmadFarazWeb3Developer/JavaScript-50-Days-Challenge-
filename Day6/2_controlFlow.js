"use strict";


const month = "march";

// Switch statement to check the month
switch (month) {
  case "jan":
    console.log("January");
    break;
  case "feb":
    console.log("February");
    break;
  case "march":
    console.log("March");
    break;
  case "april":
    console.log("April");
    break;
  default:
    console.log("Default case match");
    break;
}
